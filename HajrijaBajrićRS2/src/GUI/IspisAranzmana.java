    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dm = (DefaultTableModel) jTable1.getModel();
        int rowCount = dm.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            dm.removeRow(i);
        }
        
        ResultSet rezultat=null;   
        try {
            Konekcija konekcija=new Konekcija();
            rezultat=konekcija.ispisAranzmana();
            while(rezultat.next())
            {
                String osoba=rezultat.getString("osoba");
                String ime=rezultat.getString("ime");
                String brOdraslih=rezultat.getString("brOdraslih");
                int brO;
                brO = Integer.parseInt(brOdraslih);
                
                String brDjece=rezultat.getString("brDjece");
                int brD;
                brD = Integer.parseInt(brDjece);
                
                String podaci[]={osoba, ime, brOdraslih, brDjece};
                DefaultTableModel tabela=(DefaultTableModel)jTable1.getModel();
                tabela.addRow(podaci);
                
            }
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

